    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        // TODO add your handling code here:

        if(validateFields(jListAuthors, jTextFieldName, jTextFieldPrice, jTextFieldPublisher, jTextFieldEdition, jTextFieldLanguage, jTextFieldISBN))
        {
            book.setName(jTextFieldName.getText());
            book.setPrice(Double.parseDouble(jTextFieldPrice.getText()));
            book.setPublisher(jTextFieldPublisher.getText());
            book.setEdition(Integer.parseInt(jTextFieldEdition.getText()));
            book.setLanguage(jTextFieldLanguage.getText());
            book.setISBN(jTextFieldISBN.getText());

            if(book.save())
            {
                book.removeAllAuthors();
                for(Object author : jListAuthors.getSelectedValuesList())
                {
                    book.addAuthor((Author)author);
                }
                JOptionPane.showMessageDialog(this, "Se ha modificado con exito!");
//                jButtonClearActionPerformed(evt);
                frame.loadTableContent();
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Ocurrio un error al guardar el libro!");
            }
        }
        else
        {
            Helper.showEmptyFieldsMessage(this);
        }
    }//GEN-LAST:event_jButtonAddActionPerformed

