        private void loadFields()
        {
            jTextFieldName.setText(book.getName());
            jListAuthors.setSelectedIndices(selectedAuthors());
            jTextFieldPrice.setText(String.valueOf(book.getPrice()));
            jTextFieldPublisher.setText(book.getPublisher());
            jTextFieldEdition.setText(String.valueOf(book.getEdition()));
            jTextFieldLanguage.setText(book.getLanguage());
            jTextFieldISBN.setText(book.getISBN());
        }

    private boolean validateFields(JList authors, JTextField ... fields)
    {
        if(authors.getSelectedIndex() == -1)
        {
            return false;
        }
        
        for(JTextField field : fields)
        {
            if(field.getText().equals(""))
            {
                return false;
            }
        }
        return true;
    }
    
    private Author[] authorsToArray(ArrayList<Author> a)
    {
        Author[] authors = new Author[a.size()];
        
        for(int i = 0; i < authors.length; i++)
        {
            authors[i] = a.get(i);
        }
        
        return authors;
    }
    
    private int[] selectedAuthors()
    {
        int[] indices = new int[book.getAuthors().size()];
        int aux = 0;
        
        for(int i = 0; i < book.getAuthors().size(); i++)
        {
            for(int j = 0; j < authors.length; j++)
            {
                if(authors[j].equals(book.getAuthors().get(i)))
                {
                    indices[aux++] = j;
                }
            }
        }
        return indices;
    }
